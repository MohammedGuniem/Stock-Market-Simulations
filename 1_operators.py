x = 9
y = 5

# multiplication
print(x * y)  # result = 45

# division
print(x / y)  # result = 1.8

# exponentiation: raise one number to the power of another
print(x ** y)  # result = 59049

# Quotient: the quantity produced by the division of two numbers.
print(x // y)  # result = 1

# remainder
print(x % y)  # result = 4
